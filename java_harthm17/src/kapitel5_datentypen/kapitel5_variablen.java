public class kapitel5_variablen {
    public static void main(String[] args) {
        int a = 2;  
        System.out.println(a);
        char b = 'a';
        System.out.println(b);
        double c = 14.259826;
        System.out.println(c);
        boolean t = true;
        System.out.println(t);
        boolean f = false;
        System.out.println(f);
    }
}
